/** Get all tool sequences for a given task ID, or fallback sequences. */
export function getSequencesForTask(taskId: string): ToolStep[][] {
  return TASK_TOOL_SEQUENCES[taskId] ?? FALLBACK_SEQUENCES;
}

/** Pick a single random sequence for a given task, or from all tasks if none specified. */
export function pickRandomSequence(taskId?: string | null): ToolStep[] {
  if (taskId) {
    const sequences = getSequencesForTask(taskId);
    return sequences[Math.floor(Math.random() * sequences.length)]!;
  }
  // No active task — pick from any task's sequences
  const allTaskIds = Object.keys(TASK_TOOL_SEQUENCES);
  const randomTaskId = allTaskIds[Math.floor(Math.random() * allTaskIds.length)]!;
  const sequences = TASK_TOOL_SEQUENCES[randomTaskId]!;
  return sequences[Math.floor(Math.random() * sequences.length)]!;
}
